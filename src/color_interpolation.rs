pub fn get_color(specie_n: usize, specie_i: usize) -> egui::Rgba {
    let i = (COLORS.len() * specie_i) as f32 / specie_n as f32;
    let lo = i as usize;
    let t = i - (lo as f32);
    assert!((0.0..1.0).contains(&t));
    COLORS[lo] * (1.0 - t) + COLORS[lo + 1] * t
}
